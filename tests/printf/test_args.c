#include "test_printf.h"

void	tp_test_args(void)
{
	TP_PRINTF("%2$d // %1$d // %1$#x", 42, 21);
	TP_PRINTF("%*u // %.*u", 4, 42, 8, 21);
	TP_PRINTF("%3$*1$.*2$u // %4$*2$.*1$u", 4, 8, 42, 21);
	TP_PRINTF("%2$*1$u // %2$.*1$u", -4, 42);
	TP_PRINTF("%1$d // %% // %1$d", 42);
}
